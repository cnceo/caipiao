            <p>（二）正码</p>
                        香港⑥合彩公司每期开出的前面六个号码为正码，下注号码如在六个正码号码里中奖。
            <p>1. 总和大小</p>
            总和大：以七个开奖号码的分数总和大于或等于175。
            <br>总和小：以有七个开奖号码的分数总和小于或等于174。
            <p>2. 总和单双</p>
            总和单：以七个开奖号码的分数总和是‘单数’，如分数总和是133、197。
            <br>总和双：以七个开奖号码的分数总和是‘双数’，如分数总和是120、188。
            <p>3. 正码特</p>
            当期开奖的前面6个号码，按开奖出来的先后顺序依次分为：正1特、正2特、正3特、正4特、正5特、正6特。其下注的正码特号与现场摇珠开出之正码其开奖顺序及开奖号码相同，视为中奖。
            <br>如：19,47,22,12,40,36+X 投注，正1特-19、正2特-47、正3特-22、正4特-12、正5特-40、正6特-36 当中的任意一项都中奖，投注正码特，其它号码如，正1特-47、正4特-36 不中奖。
            <p>4. 正码1-6</p>
            香港⑥合彩公司当期开出之前6个号码叫正码。第一时间出来的叫正码1，依次为正码2、正码3┅┅ 正码6(并不以号码大小排序)。正码1、正码2、正码3、正码4、正码5、正码6的大小单双合单双和特别号码的大小单双规则相同，如正码1为31，则正码1为大，为单，为合双，为合小；正码2为08，则正码2为小，为双，为合双，为合大；号码为49则为和。假如投注组合符合中奖结果，视为中奖。正码1-6色波下注开奖之球色与下注之颜色相同时，视为中奖。其余情形视为不中奖。
            <p>5. 尾数</p>
            含有所属尾数的一个或多个号码，但派彩只派一次，即不论同尾数号码出现一个或多个号码都只派彩一次。
            <br>"0"尾：10、20、30、40
            <br>"1"尾：01、11、21、31、41
            <br>"2"尾：02、12、22、32、42
            <br>"3"尾：03、13、23、33、43
            <br>"4"尾：04、14、24、34、44
            <br>"5"尾：05、15、25、35、45
            <br>"6"尾：06、16、26、36、46
            <br>"7"尾：07、17、27、37、47
            <br>"8"尾：08、18、28、38、48
            <br>"9"尾：09、19、29、39、49
            <br>例如：开奖结果正码号码为11、31、42、44、35、32特别号码为21则1尾2尾4尾5尾都为中奖，其他尾数都不中奖。（请注意:49亦算输赢，不为和）。
            <p>（三）连码</p>
            1. 三中二：所投注的每三个号码为一组合，若其中2个是开奖号码中的正码，即为三中二，视为中奖；若3个都是开奖号码中的正码，即为三中二之中三，其余情形视为不中奖，如06、07、08 为一组合，开奖号码中有06、07两个正码，没有08，即为三中二，按三中二赔付；如开奖号码中有06、07、08三个正码，即为三中二之中三，按中三赔付；如出现1个或没有，视为不中奖 。
            <br>2. 四全中：选择投注号码每四个为一组（四个或四个以上），兑奖号为正码，如四个号码都在开奖号码的正码里面，视为中奖，其他情形都视为不中奖 。
            <br>3. 三全中：所投注的每三个号码为一组合，若三个号码都是开奖号码之正码，视为中奖，其余情形视为不中奖。如06、07、08三个都是开奖号码之正码，视为中奖，如两个正码加上一个特别号码视为不中奖 。
            <br>4. 二全中：所投注的每二个号码为一组合，二个号码都是开奖号码之正码，视为中奖，其余情形视为不中奖（含一个正码加一个特别号码之情形）。
            <br>5. 二中特：所投注的每二个号码为一组合，二个号码都是开奖号码之正码，叫二中特之中二；若其中一个是正码，一个是特别号码，叫二中特之中特；其余情形视为不中奖 。
            <br>6. 特串：所投注的每二个号码为一组合，其中一个是正码，一个是特别号码，视为中奖，其余情形视为不中奖（含二个号码都是正码之情形） 。
            <p>（四）生肖</p>
            <p>1. 特码生肖</p>
            天肖：兔马猴猪牛龙。
            <br>地肖：鼠虎蛇羊鸡狗。
            <br>家肖：羊马牛猪狗鸡。
            <br>野肖：猴蛇龙兔虎鼠。
            <br>前肖：鼠牛虎兔龙蛇。
            <br>后肖：马羊猴鸡狗猪。
            <p>2. 特码生肖</p>
            生肖顺序为 鼠 >牛 >虎 >兔 >龙 >蛇 >马 >羊 >猴 >鸡 >狗 >猪 。
            <br>如今年是猴年，就以猴为开始，依顺序将49个号码分为12个生肖『如下』
            <br>猴：01、13、25、37、49
            <br>鸡：12、24、36、48
            <br>狗：11、23、35、47
            <br>猪：10、22、34、46
            <br>鼠：09、21、33、45
            <br>牛：08、20、32、44
            <br>虎：07、19、31、43
            <br>兔：06、18、30、42
            <br>龙：05、17、29、41
            <br>蛇：04、16、28、40
            <br>马：03、15、27、39
            <br>羊：02、14、26、38
            <br>若当期特别号，落在下注生肖范围内，视为中奖 。
            <br>例如：开奖：XXXXXX+18 为特码-兔，投注特码-兔即中奖，投注特码其它生肖不中奖。
            <p>3. 正肖</p>
            当期开奖的前6个号码(不含特码，不分先后顺序)，其中有一个球号在投注的生肖范围即算中奖。如果有多个球号开在投注生肖范围内，派彩金额将自动倍增。
            <p>4. 一肖</p>
            当期开奖的全部号码(前6个号码和特码)，其中只要有一个球号在投注的生肖范围则中奖；没有一个球号在投注的生肖范围内，则不中奖；多个球号在投注生肖范围内，则中奖；但奖金不倍增，派彩只派一次，即不论同生肖号码出现一个或多个号码都只派一次。
            <p>（四）对碰</p>
            1. 生肖（尾数）所对应的号码和特码生肖（尾数）项目的一样；一个生肖（尾数）对应多个号码，不论同生肖（尾数）的号码出现一个或多个，派彩只派一次。每个生肖（尾数）都有自己的赔率，下注组合的总赔率，取该组合生肖（尾数）的最低赔率为总赔率。
            <br>2. 二连尾:选择二个尾数为一投注组合进行下注。该注的二个尾数必须在当期开出的7个开奖号码相对应的尾数中，视为中奖。
            <br>3. 三连尾:选择三个尾数为一投注组合进行下注。该注的三个尾数必须在当期开出的7个开奖号码相对应的尾数中，视为中奖。
            <br>4. 四连尾:选择四个尾数为一投注组合进行下注。该注的四个尾数必须在当期开出的7个开奖号码相对应的尾数中，视为中奖。
            <br>5. 五连尾:选择五个尾数为一投注组合进行下注。该注的五个尾数必须在当期开出的7个开奖号码相对应的尾数中，视为中奖。
            <br>6. 二连肖:选择二个生肖为一投注组合进行下注。该注的二个生肖必须在当期开出的7个开奖号码相对应的生肖中，视为中奖。
            <br>7. 三连肖:选择三个生肖为一投注组合进行下注。该注的三个生肖必须在当期开出的7个开奖号码相对应的生肖中，视为中奖。
            <br>8. 四连肖:选择四个生肖为一投注组合进行下注。该注的四个生肖必须在当期开出的7个开奖号码相对应的生肖中，视为中奖。
            <br>9. 五连肖:选择五个生肖为一投注组合进行下注。该注的五个生肖必须在当期开出的7个开奖号码相对应的生肖中，视为中奖。
            <p>（五）合肖</p>
            挑选2-11个生肖『排列如同生肖』为一个组合，并选择开奖号码的特码是否在此组合内『49号除外』，即视为中奖；若当期特码开出49号，则所有组合皆视为和局。
            <p>（六）自选不中
                <p>
                    挑选最少6个号码为一投注组合进行下注。当期开出的7个开奖号码都没有在该下注组合中，即视为中奖。每个号码都有自己的赔率，下注组合的总赔率，取该组合号码的最低赔率为总赔率。如下注组合为1-2-3-4-5-13，开奖号码为6，7，8，9，10，11，12，即为中奖，如果开奖号码为5，6，7，8，9，10，11，则为不中奖。
                    <p>（七）总肖</p>
                    当期号码(所有正码与最后开出的特码)开出的不同生肖总数，与所投注之预计开出之生肖总和数(不用指定特定生肖)，则视为中奖，其余情形视为不中奖。例如：如果当期号码为19、24、12、34、40、39 特别号：49，总计六个生肖，若选总肖【6】则为中奖(请注意：49号亦算输赢，不为和）。
                    <p>（八）总肖单双</p>
                    当期号码（正码和特码）开出的不同生肖总数若为单数则为单，若为双数则为双。
