            <p>7. 特码合数大小</p>
            以特码个位和十位数字之和值来判断胜负，和值大于6为合数大，如07、17、26、36、44；和值小于7为合数小，如01、12、23、32、42；开出49则视为和局。
            <p>8. 半波</p>
            以特码色波和特单，特双，特大，特小为一个投注组合，当期特码开出符合投注组合，即视为中奖； 若当期特码开出49号，则视为和局；其余情形视为不中奖。
