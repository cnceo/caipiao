	        <br>
	        
	        <b><p id="1">一、玩法类型及承销</p></b>中国福利彩票"重庆幸运农场" 游戏，由中国福利彩票发行管理中心（以下简称“中福彩中心”）统一发行，由北京市福利彩票发行中心承销。
            <br>
            <br>
            
            <b><p id="2">二、开奖与购买方式</p></b>
                 1. 本站重庆幸运农场每天开97期，北京时间（GMT+8）每天白天从上午10：00开到凌晨02：00，每期间隔10分钟。销售期号以开奖日界定，按日历年度编排。
            <br> 2. 购买者可透过本站进行投注，投注时需先选择相应的房间，不同房间赔率返点不一样，返点越高投注赔率则低。
            <br> 3. 如果用户投注成功后，若因销售终端故障、通讯线路故障和投注站信用额度受限等原因造成当期无法开奖的，应退还购买者投注金额。
            <br>
            <br>
            
            <b><p id="3">三、兑奖</p></b> 1. 返奖：无论大小奖均返还至用户在本站的账户中，一旦用户中奖，系统将自动返还中奖金额。可继续投注或提款，永无弃奖。
            <br>
            <br>
            
            <b><p id="4">四、玩法说明</p></b>
                 1．"重庆幸运农场"采用按位置顺序选择投注号码、固定设奖的玩法。每期重庆幸运农场开奖球数共八粒。每粒球除了总和玩法，其它都有单独的投注页面
            <br> 2. 在本站中，重庆幸运农场支持的玩法有“双面”、“数字盘”、“中发白”、“方位”和“龙虎”，玩法如下：
