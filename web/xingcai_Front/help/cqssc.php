	        <br>
	        
	        <b><p id="1">一、玩法类型及承销</p></b> 时时彩是一种在线即开型彩票玩法，属于基诺型彩票，由市福利彩票发行管理中心负责承销。
	        
	        <b><p id="2">二、开奖与购买方式</p></b> 1. 本站重庆时时彩游戏每天进行仅120期，开奖时间为白天10:00至22:00，每隔10分钟开奖一次；夜场22:00至凌晨02:00，每5分钟开奖一次。购买者可在对其选定的投注号码进行投注，返点越高则投注赔率越低。
	        <br> 2. 购买者可选择机选号码投注、自选号码投注。机选号码投注是指由投注机随机产生投注号码进行投注，自选号码投注是指将购买者选定的号码输入投注机进行投注。
	        <br> 3. 购买者还可进行追号投注。追号投注是指将一注或一组号码进行两期或两期以上的投注。追号可分为连续追号和间隔追号，连续追号指追号的期数是连续的，间隔追号指追号的期数不连续。
	        <br> 4. 如果用户投注成功后，若因销售终端故障、通讯线路故障和投注站信用额度受限等原因造成当期无法开奖的，应退还购买者投注金额。
	        <br>
	        <br>
	        
	        <b><p id="3">三、兑奖</p></b> 1. 返奖：无论大小奖均返还至用户在本站的账户中，一旦用户中奖，系统将自动返还中奖金额。可继续投注或提款，永无弃奖。
	        <br>
	        <br>
	        
	        <b><p id="4">四、玩法说明</p></b> 1. 时时彩投注区分为万位、千位、百位、十位和个位，各位号码范围为0～9。每期从各位上开出1个号码作为中奖号码，即开奖号码为5位数。时时彩玩法即是竞猜5位开奖号码的全部号码、部分号码或部分号码特征。
