            <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<b style="color: red;">牛双：</b>牛2，牛4，牛6，牛8，牛牛 为 牛双(若开出斗牛结果为没牛，则投注牛单牛双皆为不中奖)
