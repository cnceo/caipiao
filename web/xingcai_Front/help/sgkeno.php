                <b><p id="1">一、玩法类型</p></b>
               <p>新加坡28是一种在线数字竞猜游戏。共28个投注号码，押中即可获得奖励。</p><br>

                <b><p id="2">二、开奖与购买方式</p></b>
                <p>1.新加坡28开奖结果源于新加坡本地乐透Lotto开奖号码，两分钟一期，全天660期不停开奖（北京时间早上8：02至第二天早上6：00）。Lotto每期从01-80共80个号码中开出20个数字。新加坡28将这20个开奖号码按照由小到大的顺序依次排列，舍去首末两个数字，剩余的数字分别按一、二、三区的顺序分配，再分别将每个区的开奖号码相加，所得到的每个和值的尾数作为新加坡28的开奖数值，最后再将这三个和值的尾数相加即为新加坡28的最终开奖结果。<br>
                以Lotto第 2622627期开奖结果为例,将开奖号码从小到大排列后，结果如下：</p><br>
                <p><img src="/help/picture/rule.xjp28_1.png" width="710"></p><br>

                <p>首末两位除外，将号码分别按顺序位置分为三个区，并将各区开奖号码相加取和值的尾数：<br>
                    第一区：（2、5、8、11、14、17）<br>
                    第二区：（3、6、9、12、15、18）<br>
                    第三区：（4、7、10、13、16、19）</p><br>
                <p><img src="/help/picture/rule.xjp28_2.png" width="710"></p><br>
                <p>最后计算尾数的和值即为最终开奖结果：</p><br>
                <p><img src="/help/picture/rule.xjp28_3.png" width="710"></p><br>
                <p>2.购买者可透过本站进行投注，在对其选定的投注号码进行投注；或返点投注，返点越高投注赔率则低。</p>
                <p>3.购买者可选择机选号码投注、自选号码投注。机选号码投注是指由投注机随机产生投注号码进行投注，自选号码投注是指将购买者选定的号码输入投注机进行投注。 </p>
                <p>4.购买者还可进行多倍投注、追号投注。多倍投注是指同样的投注号码进行翻倍数额购买的投注。追号投注指将一注或一组号码进行两期或两期以上的投注。 </p>
                <p>5.如果用户投注成功后，若因销售终端故障、通讯线路故障和投注站信用额度受限等原因造成当期无法开奖的，退还购买者投注金额。</p>
                <b><p id="3">三、兑奖</p></b>
                <p>用户一旦中奖，无论奖金大小，系统将自动返还至用户账户中。用户可继续投注或提款，永无弃奖。</p><br>

